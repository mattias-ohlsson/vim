		Lektion 5.1: HUR MAN KÖR ETT EXTERNT KOMMANDO


   ** Skriv  :!	följt av ett externt kommando för att köra det kommandot. **

  1. Skriv det välbekanta kommandot  :  för att placera markören längst ned
     på skärmen.  Detta låter dig skriva in ett kommando.

  2. Skriv nu  !  (utropstecken).  Detta låter dig köra ett godtyckligt
     externt skalkommando.

  3. Som ett exempel, skriv  ls  efter ! och tryck sedan <ENTER>.  Detta
     kommer att visa dig en listning av din katalog, precis som om du kört det
     vid skalprompten.  Använd  :!dir  om ls inte fungerar.

NOTERA:  Det är möjligt att köra godtyckligt externt kommando på det här
         sättet, även med argument.

NOTERA:  Alla  :-kommandon måste avslutas med att trycka på <ENTER>.  Från
         denna punkt är det inte alltid vi nämner det.


~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
		      Lektion 5.2: MER OM ATT SPARA FILER


     ** För att spara ändringar gjorda i en fil, skriv  :w FILNAMN. **

  1. Skriv  :!dir  eller  :!ls  för att få en listning av din katalog.
     Du vet redan att du måste trycka <ENTER> efter det här.

  2. Välj ett filnamn som inte redan existerar, som t.ex. TEST.

  3. Skriv nu:  :w TEST  (där TEST är filnamnet du valt.)

  4. Det här sparar hela filen (Vim-handledningen) under namnet TEST.
     För att verifiera detta, skriv  :!ls  eller  :!dir  igen för att se din
     katalog

NOTERA: Om du skulle avsluta Vim och sedan öppna igen med filnamnet TEST så
	skulle filen vara en exakt kopia av handledningen när du sparade den.

  5. Ta nu bort filen genom att skriva (MS-DOS):  :!del TEST
				   eller (Unix):  :!rm TEST

~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
		    Lektion 5.3: ETT SELEKTIVT SPARA-KOMMANDO

     ** För att spara en del av en fil, skriv  v  rörelse  :w FILNAMN **

  1. Flytta markören till den här raden.

  2. Tryck  v  och flytta markören till det femte punkten nedanför.  Notera
     att texten blir markerad.

  3. Skriv  :-tecknet.  Längst ner på skärmen visas  :'<,'>

  4. Skriv  w TEST  , där TEST är ett filnamn som ännu inte existerar.
     Verifiera att du ser  :'<,'>w TEST  innan du trycker på <ENTER>.

  5. Vim kommer att skriva den markerade texten till filen TEST.  Använd
     :!dir  eller  :!ls  för att se den.  Radera den inte än!  Vi kommer att
     använda den i nästa lektion.

NOTERA: Genom att trycka  v  startas visuell markering.  Du kan förflytta
        markören för att göra markeringen större eller mindre.  Därefter kan
        du använda ett kommando för att göra något med texten.  Till exempel
        d   raderar texten.
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
		   Lektion 5.4: TA EMOT OCH FÖRENA FILER


       ** För att infoga innehållet av en fil, skriv  :r FILNAMN  **

  1. Placera markören precis ovanför denna rad.

NOTERA: Efter att ha utfört steg 2 kommer du att se texten från Lektion 5.3.
        Gå därefter NEDÅT för att se den här lektionen igen.

  2. Ta nu emot din TEST-fil med kommandot  :r TEST  där TEST är namnet på
     filen du använde tidigare.
     Filen du tar emot placeras nedanför markörens rad.

  3. För att verifiera att filen togs emot, gå tillbaka och notera att det
     nu finns två kopior av Lektion 5.3, orginalet och filversionen.

NOTERA: Du kan även läsa utmatningen av ett externt kommando.  Exempelvis
        :r !ls  läser utmatningen av ls-kommandot och lägger det nedanför
        markören.


~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			       LEKTION 5 SAMMANFATTNING


  1.  :!kommando  kör ett externt kommando.

      Några användbara exempel är:
	 (MS-DOS)	  (Unix)
	  :!dir		   :!ls		  -  visar en kataloglistning.
	  :!del FILNAMN    :!rm FILNAMN   -  tar bort filen FILNAMN.

  2.  :w FILNAMN  sparar den aktuella Vim-filen med namnet FILNAMN.

  3.  v  rörelse  :w FILNAMN  sparar de visuellt markerade raderna till filen
      FILNAMN.

  4.  :r FILNAMN  tar emot filen FILNAMN och infogar den i den aktuella filen
      nedanför markören.

  5.  :r !dir  läser utmatningen från dir-kommandot och infogar det nedanför
      markören.


~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			 Lektion 6.1: ÖPPNA-KOMMANDOT


 ** Skriv  o  för att öppna en rad under markören och placera dig i
    Infoga-läge. **

  1. Flytta markören till raden nedan markerad --->.

  2. Skriv gemenen  o  för att öppna upp en rad NEDANFÖR markören och placera
     dig i Infoga-mode.

  3. Skriv in lite text och tryck på <ESC> för att avsluta Infoga-läget.

---> Efter du skrivit  o  placeras markören på en öppen rad i Infoga-läge.

  4. För att öppna upp en rad OVANFÖR markören, skriv ett stort  O , istället
     för ett litet  o.  Pröva detta på raden nedan.

---> Öppna en rad ovanför denna genom att trycka O när markören står här.



~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			Lektion 6.2: LÄGG TILL-KOMMANDOT

	     ** Skriv  a  för att infoga text EFTER markören. **

  1. Flytta markören till början av raden nedan markerad --->.

  2. Tryck  e  till markören står på slutet av  ra .

  3. Skriv ett  a  (litet a) för att lägga till text EFTER markören.

  4. Slutför ordet som det på raden under.  Tryck <ESC> för att avsluta
     Infoga-läget.

  5. Använd  e  för att gå till nästa ofullständiga ord och upprepa steg 3 och
     4.

---> Denna ra låter dig trä på att läg till text på en rad.
---> Denna rad låter dig träna på att lägga till text på en rad.

NOTERA: a, i och A går alla till samma Infoga-läge.  Den enda skillnaden är
        var tecknen blir infogade.

~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
		    Lektion 6.3: EN ANNAN VERSION AV ERSÄTT


      ** Skriv ett stort  R  för att ersätta fler än ett tecken. **

  1. Flytta markören till den första raden nedan markerad --->.  Flytta
     markören till början av första  xxx .

  2. Tryck nu  R  och skriv in numret som finns på den andra raden nedanför,
     så det ersätter xxx.

  3. Tryck <ESC> för att lämna Ersätt-läget.  Lägg märke till att resten av
     raden förblir oförändrad.

  4. Upprepa stegen för att ersätta den kvarvarande xxx.

---> Genom att addera 123 till xxx får vi xxx.
---> Genom att addera 123 till 456 får vi 579.

NOTERA: Ersätt-läget är likt Infoga-läget, men varje tecken som skrivs
        kommer att ersätta ett befintligt tecken.

~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
		    Lektion 6.4: KOPIERA OCH KLISTRA IN TEXT

  ** Använd y-operatorn för att kopiera text och  p  för att klista in den **

  1. Gå till raden markerad ---> nedan och placera markören efter "a)".

  2. Starta visuellt läge med  v  och flytta markören till precis innan
     "första".

  3. Tryck  y  för att kopiera (engelska: "yank") den markerade texten.

  4. Flytta markören till slutet av nästa rad:  j$

  5. Tryck  p  för att klistra in texten. Skriv därefter:  a andra <ESC> .

  6. Använd visuellt läge för att välja " posten.", kopiera med  y , gå till
     slutet av nästa rad med  j$  och klistra in texten där med  p .

---> a) Detta är den första posten.
     b)

NOTERA: Du kan även använda  y  som en operator;  yw  kopierar ett ord.
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			    Lektion 6.5: SÄTT FLAGGOR


  ** Sätt en flagga så att en sökning eller ersättning ignorerar storlek **

  1. Sök efter "ignorera" genom att skriva:  /ignorera  <ENTER>
     Repetera flera gånger genom att trycka  n .

  2. Sätt flaggan 'ic' (Ignore Case) genom att skriva:  :set ic

  3. Sök nu efter "ignorera" igen genom att trycka:  n
     Notera att både "Ignorera" och "IGNORERA" hittas.

  4. Sätt flaggorna 'hlsearch' och 'incsearch':  :set hls is

  5. Skriv nu in sökkommandot igen, och se vad som händer:  /ignorera <ENTER>

  6. För att slå av ignorering av storlek, skriv:  :set noic

NOTERA: För att ta bort framhävningen av träffar, skriv:  :nohlsearch
NOTERA: Om du vill ignorera storlek för ett enstaka sökkommando, använd  \c
        i sökfrasen:  /ignorera\c  <ENTER>
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			       LEKTION 6 SAMMANFATTNING

  1. Skriv  o  för att öppna en rad NEDANFÖR markören och starta Infoga-läget
     Skriv  O  för att öppna en rad OVANFÖR markören.

  2. Skriv  a  för att infoga text EFTER markören.
     Skriv  A  för att infoga text efter slutet av raden.

  3. Kommandot  e  går till slutet av ett ord.

  4. Operatorn  y  kopierar ("yank") text,  p  klistrar in ("paste") den.

  5. Genom att skriva  R  startas ersättningsläge till  <ESC>  trycks.

  6. Skriv ":set xxx" för att sätta flaggan "xxx".  Exempel på flaggor är:
	'ic' 'ignorecase'	ignorer storlek på bokstäver vid sökning
	'is' 'incsearch'	visa delvisa matchningar för en sökfras
	'hls' 'hlsearch'	framhäv alla matchande fraser
     Du kan använda både det långa och det korta flaggnamnet.

  7. Lägg till "no" före flaggan för att slå av den:  :set noic

